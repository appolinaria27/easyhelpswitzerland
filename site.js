window.translations = translations;

function getTranslation(lang, key) {
  if (
    translations[lang] &&
    Object.prototype.hasOwnProperty.call(translations[lang], key)
  ) {
    return translations[lang][key];
  }

  if (
    translations.en &&
    Object.prototype.hasOwnProperty.call(translations.en, key)
  ) {
    return translations.en[key];
  }

  return null;
}

function setLanguage(lang) {
  const safeLang = translations[lang] ? lang : "en";

  localStorage.setItem("siteLanguage", safeLang);
  document.documentElement.setAttribute("lang", safeLang);

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n;
    const value = getTranslation(safeLang, key);

    if (value === null) return;

    if (el.hasAttribute("data-i18n-html")) {
      el.innerHTML = value;
    } else {
      el.textContent = value;
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.dataset.i18nPlaceholder;
    const value = getTranslation(safeLang, key);

    if (value !== null) {
      el.setAttribute("placeholder", value);
    }
  });

  document.querySelectorAll("[data-i18n-title]").forEach((el) => {
    const key = el.dataset.i18nTitle;
    const value = getTranslation(safeLang, key);

    if (value !== null) {
      if (el.hasAttribute("title")) {
        el.setAttribute("title", value);
      }

      if (el.hasAttribute("data-title")) {
        el.setAttribute("data-title", value);
      }
    }
  });

  document.querySelectorAll("[data-i18n-content]").forEach((el) => {
    const key = el.dataset.i18nContent;
    const value = getTranslation(safeLang, key);

    if (value !== null && el.hasAttribute("data-content")) {
      el.setAttribute("data-content", value);
    }
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach((el) => {
    const key = el.dataset.i18nAriaLabel;
    const value = getTranslation(safeLang, key);

    if (value !== null) {
      el.setAttribute("aria-label", value);
    }
  });

  document.querySelectorAll("[data-lang]").forEach((btn) => {
    btn.classList.remove("active");
  });

  document.querySelectorAll(`[data-lang="${safeLang}"]`).forEach((btn) => {
    btn.classList.add("active");
  });
}

document.addEventListener("DOMContentLoaded", () => {
  const savedLang = localStorage.getItem("siteLanguage") || "en";
  setLanguage(savedLang);

  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.addEventListener("click", () => {
      const lang = button.getAttribute("data-lang");
      if (lang) {
        setLanguage(lang);
      }
    });
  });
});